package day03_comments_escape_sequence;

public class TabAndNextLine {
    public static void main(String[] args) {
        System.out.println("My name starts with \'I\'");
        System.out.println("\t This is tabbed ones");
        System.out.println("\t \tThis is tabbed twice");
        System.out.println("     This is tabbed without escape ");

        System.out.println("\t\t\t\texample ");
        System.out.println("                example");
        //example of next line
        System.out.println("");
        System.out.println("1)Go to store ");
        System.out.println("2)grab milk");
        System.out.println("3)wash car");
        System.out.println("\n1)Go to store");

    }
}
