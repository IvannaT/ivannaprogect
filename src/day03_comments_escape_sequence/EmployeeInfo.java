package day03_comments_escape_sequence;

public class EmployeeInfo {
    public static void main(String[] args) {
        System.out.println("Company name :    Google");
        System.out.println("Employee :        Khalid");
        System.out.println("ID:               12345");
        System.out.println("Job Title :       SDET");


        System.out.println("Company name:\t\tGoogle \nEmployee:\t\t\tKhalid \nID:\t\t\t\t\t12345 \nJob Title:\t\t\tSDET");
    }
}
