package day04_variables_intro;

public class FirstVariable {
    public static void main(String[] args) {
        //declare variable n that can store int (whole number )
        int n;
        //assign 10 to n
        n = 10;
        //print value of n variable
        System.out.println(n);
        System.out.println("n");

        int apples;
        apples = 50;
        System.out.println(apples);
        int tea;
        tea = 33;
        System.out.println(tea);
        int cream;
        cream =46;
        System.out.println(cream);
        //declare variable bananas,assign 23,print value out
        int bananas;
        bananas = 23;
        System.out.println(bananas);

    }
}
