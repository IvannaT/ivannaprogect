package day04_variables;

public class PrimitiveDataTypes {
    public static void main(String[] args) {

        byte age = 60;
        System.out.println("age");
        System.out.println(age);

        short year;//declare a variable called year.It is short type
        year = 2022;//assigned 2022into my year variable
      //  year = 2023;//reassigns the value to be 2023

        int addressNumber = 2222222;

        //long bigNumber = 123456789087665555;the value is int type ,is to big for int
        long bigNumber;
        bigNumber = 12233445566L;
        System.out.println(year);
        System.out.println(addressNumber);
        System.out.println(bigNumber);

    }
}
