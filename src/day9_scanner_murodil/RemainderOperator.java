package day9_scanner_murodil;

public class RemainderOperator {
    public static void main(String[] args) {
        System.out.println(10 / 3);//3
        System.out.println(10 % 3);//1 3+3+3+1
        System.out.println(10 / 5);//2
        System.out.println(10 % 5);//0 5+5=0
        System.out.println(20 / 7);//2  = 7+7=14 /=2
        System.out.println(20 % 7);//6  = 7+7=14 //20-14=6
        System.out.println(100 % 60);



    }
}
