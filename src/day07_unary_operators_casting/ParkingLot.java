package day07_unary_operators_casting;

public class ParkingLot {
    public static void main(String[] args) {
        int cars = 10;

       // cars = cars + 2;
        System.out.println(cars);
        System.out.println("2 cars more drive in");
        cars++;
        cars++;
        System.out.println(cars);
        System.out.println("5 cars left");
        cars --;
        System.out.println(cars);

    }
}